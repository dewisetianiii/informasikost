            <h2 class="mb-3">TIPS MENCEGAH KORSLETING LISTRIK DAN KEBAKARAN</h2>
            <p>Tidak jarang kita mendengar berita kebakaran yang dsebabkan oleh korsleting listrik. 
            Korsleting listrik sendiri berasal dari Bahasa Belanda yaitu kortsluiting yang berarti 
            hubungan pendek. Hubungan pendek atau korsleting adalah suatu hubungan dengan tahanan
             listrik yang sangat kecil, mengakibatkan aliran listrik yang sangat besar dan bila 
             tidak ditangani dapat mengakibatkan ledakan dan kebakaran. Kalian tentu tidak 
             menginginkan musibah kebakaran menimpa kalian kan? Maka dari itu, mulai lah untuk
              memperhatikan alat-alat di kos yang dialiri listrik. Nah pada artikel ini akan 
              menjelaskan Tips Mencegah Korsleting Listrik dan Kebakaran.</p>
            <p>
              <img src="assets/frontend/images/lampu.jpg" alt="" class="img-fluid">
            </p>
            <h2 class="mb-3 mt-5">#1. Hindari Menumpuk Steker Terlalu Banyak</h2>
            <p>
              <img src="assets/frontend/images/steker.jpg" alt="" class="img-fluid">
            </p>
            <p>Meskipun terlihat sepele, tapi menumpuk steker atau colokan listrik ini perlu diperhatikan dengan baik lho! Pasalnya, terlalu banyak menumpuk steker pada satu stopkontak dapat menimbulkan korsleting dan kebakaran. Karena sambungan seperti itu akan terus menerus menumpuk panas yang akhirnya dapat mengakibatkan korsleting listrik. Jadi mulai sekarang, hindarilah menumpuk steker di satu stopkontak ya!</p>
            <h2 class="mb-3 mt-5">#2.  Menggunakan Peralatan Listrik SNI</h2>
            <p>
              <img src="assets/frontend/images/lampu.jpg" alt="" class="img-fluid">
            </p>
            <p>Sejatinya, seluruh perlengkapan yang dibutuhkan dalam kehidupan sehari-hari sudah memiliki standar. Pemerintah sudah menetapkan standar tersebut dengan melebeli SNI pada setiap benda yang sudah memenuhi Standar Nasional Indonesia. Begitu pula dengan alat listrik, gunakanlah peralatan listrik yang memenuhi standar SNI dan ukuran yang tepat. Jangan asal menggunakan tanpa memahami standard an ukurannya ya!</p>
            <h2 class="mb-3 mt-5">#3. Menjauhkan Benda Mudah Terbakar Dari Sumber Listrik</h2>
            <p>
              <img src="assets/frontend/images/elektronik.jpg" alt="" class="img-fluid">
            </p>
            <p>Jangan biarkan peralatan elektronik kalian menancap terlalu lama di stopkontak. Seperti kabel charger, televise, laptop, jika sudah selesai menggunakannya maka langsunglah cabut dari stopkontak. Selain boros listrik, membiarkan peralatan elektronik menancap terlalu lama juga dapat menyebabkan korsleting. Jadi lebih baik menancapkan peralatan elektronik saat digunakan saja ya.<br><br>
              Nah itu dia tips menghindari korsleting dan kebakaran yang bisa kalian lakukan. Apalagi bagi anak kos yang tinggal sendiri dan sering meninggalkan kamar. Lebih baik memperhatikan penggunaan peralatan listrik untuk mencegah musibah kebakaran. </p>
