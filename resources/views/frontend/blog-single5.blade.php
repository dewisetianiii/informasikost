            <h2 class="mb-3">3 ALASAN MENABUNG DIAWAL BULAN</h2>
            <p>Menyisihkan sebagian dari pendapatan merupakan cara terbaik untuk menyiapkan masa depan secara finansial. Bagi sebagian orang, menabung memang bisa dikatakan cukup sulit untuk diaplikasikan. Apalagi untuk anak muda yang baru merasakan memiliki pendapatan sendiri. Rasanya banyak sekali keinginan untuk beli ini dan beli itu sampai lupa untuk menabung sebagian dari pendapatannya. Menabung memang membutuhkan kesadaran ekstra dan kemauan dari diri sendiri. Namun memaksakan diri untuk menabung ini tidak ada ruginya sama sekali loh! Pepatah mengatakan “sedikit-dikit lama-lama menjadi bukit”. Begitulah harusnya pola pikir yang kita miliki untuk memulai menabung. Tidak harus dimulai dari menyisihkan uang dalam jumlah besar. Kita bisa memulai dari jumlah kecil, yaitu 5% hingga 20% dari penghasilan. Namun, sekecil apapun jumlah uang yang ditabung sering kali tidak berhasil disisihkan karena sudah habis duluan di awal bulan. Lalu bagaimana dong agar bisa menabung? Jawabannya, ya menabung lah diawal bulan!</p>
            <h2 class="mb-3 mt-5">#1. Jadikan menabung sebagai prioritas</h2>
            <p>
              <img src="assets/frontend/images/prioritas.jpg" alt="" class="img-fluid">
            </p>
            <p>Ingat, untuk menabung selain diperlukan kesadaran juga diperlukan komitmen. Ketika kita sudah berkomitmen untuk menabung, maka menabung otomatis akan menjadi prioritas kalian. Sebagai prioritas, uang yang akan kalian sisihkan harus diutamakan untuk masuk tabungan sebelum akhirnya ludes tidak karuan. Maka dari itu, menabung diawal bulan sangatlah penting untuk menanam komitmen menabung.</p>
            <h2 class="mb-3 mt-5">#2.  Menyisihkan penghasilan untuk ditabung sebelum tagihan datang</h2>
            <p>
              <img src="assets/frontend/images/nabung.jpg" alt="" class="img-fluid">
            </p>
            <p>Bagi kalian yang sudah memiliki tanggungan, pasti merasakan bagaimana kejamnya tagihan menggerogoti diawal bulan. Biasanya tagihan-tagihan tersebut membuat kita lupa bahwa sebagian dari uang kita harus disisihkan untuk ditabung. Mungkin sebagian dari kalian pernah merasakan dimana ketika sekali mengeluarkan uang, ‘keterusan’ untuk mengeluarkan uang sehingga tidak sadar tiba-tiba uang sudah habis saja. Atau kadang karena melihat masih ada uang di rekening, kalian jadi merasa masih punya kemampuan untuk menambah tanggungan. Dan tidak kalian sadari bahwa uang kalian habis tanpa sempat kalian sisihkan. Jadi untuk menghindari hal tersebut, maka sisihkanlah uang kalian diawal bulan untuk di tabung.</p>
            <h2 class="mb-3 mt-5">#3. Ajakan teman untuk nongkrong dan diskon belanja gede-gedean</h2>
            <p>
              <img src="assets/frontend/images/belanja.jpg" alt="" class="img-fluid">
            </p>
            <p> Nah kalau yang ini tentu semua pernah mengalaminya. Siapa yang tidak tergoda dengan diskon gede-gedean yang ditawarkan oleh toko-toko belanja? Belum lagi ajakan teman yang sering kali menggunakan jurus ampuh kata-kata “jalan yuk, awal bulan habis gajian”. Jika menggunakan prinsip belanja diawal bulan dan hura-hura diawal bulan, kalian akan mengalami kesusahan untuk menyisihkan uang kalian! Karena hari masih panjang dan pengeluaran yang tak terduga bisa saja muncul. Jadi lebih baik kalian menabung diawal bulan sebelum berburu diskon atau pergi hang out dengan teman-teman. Gunakanlah uang yang tersisa untuk memenuhi keinginan kalian, bukan menabung sisa uang yang ada setelah digunakan untuk senang-senang.</p>
